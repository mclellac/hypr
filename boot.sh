clear
echo -e "\n$ansi_art\n"

sudo pacman -Syu --noconfirm --needed git

# Use custom repo if specified, otherwise default to basecamp/hypr
hypr_REPO="${hypr_REPO:-basecamp/hypr}"

echo -e "\nCloning hypr from: https://github.com/${hypr_REPO}.git"
rm -rf ~/.local/share/hypr/
git clone "https://github.com/${hypr_REPO}.git" ~/.local/share/hypr >/dev/null

# Use custom branch if instructed, otherwise default to master
hypr_REF="${hypr_REF:-master}"
if [[ $hypr_REF != "master" ]]; then
  echo -e "\eUsing branch: $hypr_REF"
  cd ~/.local/share/hypr
  git fetch origin "${hypr_REF}" && git checkout "${hypr_REF}"
  cd -
fi

echo -e "\nInstallation starting..."
source ~/.local/share/hypr/install.sh
